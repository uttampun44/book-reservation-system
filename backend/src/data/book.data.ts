/**
 * Generates Open Library cover image URLs for a given ISBN.
 * Source: https://openlibrary.org/dev/docs/api/covers
 * Rate limit: 100 requests/IP per 5 min (for non-ID lookups).
 * No API key required.
 */
const olCover = (isbn: string) => {
  const base = `https://covers.openlibrary.org/b/isbn/${isbn.replace(/-/g, "")}`;
  return {
    small: `${base}-S.jpg`,
    medium: `${base}-M.jpg`,
    large: `${base}-L.jpg`,
  };
};

