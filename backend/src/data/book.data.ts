export const getBookById = (id: string): Book | undefined =>
  books.find((b) => b.id === id);

export const getBooksByGenre = (genre: string): Book[] =>
  books.filter((b) => b.genre.toLowerCase() === genre.toLowerCase());

export const getFeaturedBooks = (): Book[] =>
  books.filter((b) => b.featured);

export const getInStockBooks = (): Book[] =>
  books.filter((b) => b.inStock);

export const searchBooks = (query: string): Book[] => {
  const q = query.toLowerCase();
  return books.filter(
    (b) =>
      b.title.toLowerCase().includes(q) ||
      b.author.toLowerCase().includes(q) ||
      b.tags.some((t) => t.toLowerCase().includes(q))
  );
};

export const getBooksSortedByRating = (): Book[] =>
  [...books].sort((a, b) => b.rating - a.rating);

export const getBooksSortedByYear = (order: "asc" | "desc" = "desc"): Book[] =>
  [...books].sort((a, b) =>
    order === "desc" ? b.publishedYear - a.publishedYear : a.publishedYear - b.publishedYear
  );

export default books;
